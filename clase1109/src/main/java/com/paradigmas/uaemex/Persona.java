package com.paradigmas.uaemex;

public class Persona {
    private Nombre nombre;
    private int edad;

    public Persona() {
    }

    public Nombre getNombre() {
        return nombre;
    }

    public void setNombre(Nombre nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

}
